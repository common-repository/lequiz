
<!-- wp:heading --><h2>C'est quoi, ce quiz ?</h2><!-- /wp:heading -->
<!-- wp:paragraph --><p>Ce quiz vous permet d'appronfondir vos connaissances de façon ludique.</p><!-- /wp:paragraph -->
<!-- wp:paragraph --><p>Plusieurs types de jeu, associés à un sablier, des jokers, un choix de graphisme et d'animations, permettent à l'application de s'adapter aux souhaits des internautes.</p><!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Trois modes de jeu sont proposés :</p><!-- /wp:paragraph -->
<!-- wp:list -->
<ul>
<li>Joker et QCM (par défaut): Ce mode permet de choisir parmi plusieurs réponses proposées.
<ul>
<li>Si le gestionnaire a défini lui même une liste de réponses possibles, aucun joker ne sera proposé.</li>
<li>Si le gesttionnaire n'a pas défini de QCM, l'internaute devra trouver le premier caractère de la réponse, parmi cinq lettres ou chiffres proposés. Le score tiendra compte des jokers utilisés.</li>
</ul>
</li> 
<li>Saisie de la réponse au clavier : ce mode de jeu, qui demande plus de temps, favorise à la fois l'apprentissage du clavier et la réflexion. L'internaute peut ajuster le sablier en fonction de la rapidité de saisie. Le score tiendra compte des frappes claviers, justes ou fausses, et des jokers utilisés.</li> 
<li>Affichage immédiat : ce mode rapide permet un affichage immédiat de la réponse, avec avec un simple clic dans la zone prévue à cet effet. Le score n'est pas calculé.</li>
</ul>
<!-- /wp:list -->
<!-- wp:paragraph --><p>Le sablier, facultatif (30 secondes par défaut), avec une diminution du score de un point toutes les cinq secondes, incite à répondre rapidement pour augmenter son score.</p><!-- /wp:paragraph -->

<!-- wp:heading --><h2>Les jokers</h2><!-- /wp:heading -->
<!-- wp:paragraph --><p>Si la réponse contient plusieurs mots, un joker permet l'affichage d'un mot, jusqu'à ce qu'il n'en reste qu'un.</p><!-- /wp:paragraph -->
<!-- wp:paragraph --><p>Lorsque la réponse ne contient qu'un mot, ou que les autres mots ont été dévoilés, les jokers suivants sont sucessivement disponibles :</p><!-- /wp:paragraph -->
<!-- wp:list -->
<ul>
<li>Joker "Nombre de caractères" du mot (par défaut, ce joker n'est pas utilisé).</li>
<li>Joker "Un caractère" : affiche un caractère, jusqu'à ce qu'il n'en reste que cinq.</li>
<li>Joker "Anagramme" du mot.</li>
</ul>
<!-- /wp:list -->
<!-- wp:paragraph --><p>Incidence des jokers sur le score : Chaque joker utilisé fait diminuer le score : les jokers "Afficher un mot" coûtent deux points, les autres coûtent un point.</p><!-- /wp:paragraph -->
<!-- wp:paragraph --><p>La personnalisation du quiz permet de ne pas prendre en compte un, plusieurs ou tous les jokers. L'internaute peut ainsi choisir le niveau de difficulté qui lui convient.</p><!-- /wp:paragraph -->

<!-- wp:heading --><h2>Calcul du score</h2><!-- /wp:heading -->
<!-- wp:paragraph --><p>Pour chaque question, le score est initialisé à 10.</p><!-- /wp:paragraph -->
<!-- wp:paragraph --><p>Le nombre de points potentiels est affiché quand l'utilisateur clique sur une zone en jaune pour proposer une réponse ou pour demander un joker.</p><!-- /wp:paragraph -->
<!-- wp:paragraph --><p>En mode clavier, le score augmente chaque fois q'un caractère correspondant à la réponse est saisi. Si la réponse comporte plusieurs mots, l'espace ne compte pas.</p><!-- /wp:paragraph --> 
<!-- wp:paragraph --><p>Le score diminue :</p><!-- /wp:paragraph -->
<!-- wp:list -->
  <ul><li>Chaque fois q'une joker est utilisé
      <ul>
         <li>De deux points pour les jokers qui affichent un mot complet, si la réponse comporte plusieurs mots.</li>
         <li>D'un point pour les autres jokers.</li>
       </ul>
       </li>  
         <li>D'un point si le joueur interrompt puis reprend le jeu, pour chaque question.</li>
         <li>D'un point toutes les cinq secondes, sauf si le sablier est désactivé.</li>
         <li>En mode clavier, d'un point chaque fois qu'un caractère ne correspondant pas à la réponse est saisi. Si la réponse comporte plusieurs mots, l'espace ne compte pas.</li> 
  </ul>
<!-- /wp:list -->  
 
<!-- wp:paragraph --><p>Le résultat est considéré comme juste, et la réponse est affichée en vert : </p><!-- /wp:paragraph --> 
<!-- wp:list -->
   <ul>
   <li>En mode QCM :
     <ul>
      <li>Si la réponse est exacte, dans le cas où le gestionnaire à défini une liste de réponses possibles.</li> 
     <li>Sinon, si la lettre ou le chiffre choisi correspond au premier carctère manquant de la réponse.</li>
     </ul>
   </li>
   <li>En mode clavier, si la saisie correspond exactement à la réponse. Les accents sur les voyelles peuvent ne pas être saisis. La saisie peut se faire indifféremment en majuscules ou en minuscules.</li>
   
   </ul>
<!-- /wp:list -->   
  
<!-- wp:paragraph --><p>Le résultat est considéré comme faux, et la bonne réponse est affichée en rouge :</p><!-- /wp:paragraph -->
<!-- wp:list -->
  <ul>
   <li>En mode QCM, si la lettre ou la réponse choisie ne correspond pas à la réponse.</li> 
  <li>En mode clavier
         <ul>
         <li>Si aprés utilisation des jokers et la saisie de plusieurs caractères, le nombre de points pour la réponse tombe à zéro.</li>
         <li>Avec un appui prolongé dans la zone de saisie.</li>
         </ul>
       </li>  
   <li>Si le sablier est activé et si aucune réponse n'est apportée dans le temps défini par ce sablier (30 secondes par défaut).</li>     
  </ul> 
 <!-- /wp:list -->  
<!-- wp:paragraph --><p>En cours de jeu, il est possible d'afficher le score ou de le réinitialier, via le lien Score / Réinitialiser"   </P><!-- /wp:paragraph -->

